<script setup lang="ts">
import { ref, onMounted, computed, reactive } from 'vue';
import { useEventStore, type IEventCard, type IEventDetail } from '@/stores/events';
import { createEventApi, updateEventApi, addEventMediaApi, deleteEventMediaApi, deleteEventApi, addActivityApi, updateActivityApi, deleteActivityApi } from '@/api/events';
import EventCard from '@/components/EventCard.vue';
import ActionModal from '@/components/ActionModal.vue';

const eventStore = useEventStore();

const selectedEvent = ref<IEventDetail | null>(null);
const form = reactive<{ data: Partial<IEventDetail> | null }>({ data: null });
const isTeam = computed({
  get: () => !!form.data?.is_team,
  set: (val: boolean) => {
    if (form.data) {
      form.data.is_team = val;
      if (!val) {
        form.data.max_teams = undefined;
      }
    }
  }
});

function validateEventForm(): string | null {
  if (!form.data) return "–ù–µ—Ç –¥–∞–Ω–Ω—ã—Ö –¥–ª—è —Å–æ—Ö—Ä–∞–Ω–µ–Ω–∏—è.";
  if (!form.data.title || form.data.title.trim() === '') return '–ù–∞–∑–≤–∞–Ω–∏–µ –º–µ—Ä–æ–ø—Ä–∏—è—Ç–∏—è –Ω–µ –º–æ–∂–µ—Ç –±—ã—Ç—å –ø—É—Å—Ç—ã–º!';
  if (!form.data.date || !/^\d{2}\.\d{2}\.\d{4}$/.test(form.data.date)) return '–£–∫–∞–∂–∏—Ç–µ –∫–æ—Ä—Ä–µ–∫—Ç–Ω—É—é –¥–∞—Ç—É –≤ —Ñ–æ—Ä–º–∞—Ç–µ –î–î.–ú–ú.–ì–ì–ì–ì!';
  if (!form.data.start_time || !/^\d{2}:\d{2}(:\d{2})?$/.test(form.data.start_time)) return '–£–∫–∞–∂–∏—Ç–µ –∫–æ—Ä—Ä–µ–∫—Ç–Ω–æ–µ –≤—Ä–µ–º—è –Ω–∞—á–∞–ª–∞ –≤ —Ñ–æ—Ä–º–∞—Ç–µ –ß–ß:–ú–ú!';
  if (!form.data.end_time || !/^\d{2}:\d{2}(:\d{2})?$/.test(form.data.end_time)) return '–£–∫–∞–∂–∏—Ç–µ –∫–æ—Ä—Ä–µ–∫—Ç–Ω–æ–µ –≤—Ä–µ–º—è –æ–∫–æ–Ω—á–∞–Ω–∏—è –≤ —Ñ–æ—Ä–º–∞—Ç–µ –ß–ß:–ú–ú!';
  if (form.data.start_time >= form.data.end_time) return '–í—Ä–µ–º—è –Ω–∞—á–∞–ª–∞ –¥–æ–ª–∂–Ω–æ –±—ã—Ç—å —Ä–∞–Ω—å—à–µ –≤—Ä–µ–º–µ–Ω–∏ –æ–∫–æ–Ω—á–∞–Ω–∏—è!';
  if (form.data.is_team && (!form.data.max_teams || form.data.max_teams <= 0)) return '–î–ª—è –∫–æ–º–∞–Ω–¥–Ω–æ–≥–æ –º–µ—Ä–æ–ø—Ä–∏—è—Ç–∏—è —É–∫–∞–∂–∏—Ç–µ –º–∞–∫—Å–∏–º–∞–ª—å–Ω–æ–µ —á–∏—Å–ª–æ –∫–æ–º–∞–Ω–¥!';
  return null; // –í–æ–∑–≤—Ä–∞—â–∞–µ–º null, –µ—Å–ª–∏ –æ—à–∏–±–æ–∫ –Ω–µ—Ç
}

const isCreatingNew = ref(false);

// –°–æ—Å—Ç–æ—è–Ω–∏–µ –¥–ª—è –≤–∫–ª–∞–¥–æ–∫
const activeTab = ref<'events' | 'activities'>('events');
const selectedEventForActivities = ref<IEventDetail | null>(null);

const showModal = ref(false);
const modalConfig = reactive({
  type: 'success' as 'success' | 'error' | 'confirm',
  title: '',
  message: '',
  onConfirm: () => {}
});

const futureEvents = computed(() => eventStore.events.filter(e => e.state === 'future'));
const currentEvents = computed(() => eventStore.events.filter(e => e.state === 'current'));
const pastEvents = computed(() => eventStore.events.filter(e => e.state === 'past'));

const selectEventForEditing = async (card: IEventCard) => {
  // –ò–°–ü–û–õ–¨–ó–£–ï–ú JSON.parse/stringify –í–ú–ï–°–¢–û structuredClone
  // –≠—Ç–æ –≥–∞—Ä–∞–Ω—Ç–∏—Ä–æ–≤–∞–Ω–Ω–æ —Å–æ–∑–¥–∞—Å—Ç —á–∏—Å—Ç—É—é, –≥–ª—É–±–æ–∫—É—é –∫–æ–ø–∏—é –æ–±—ä–µ–∫—Ç–∞ –¥–∞–Ω–Ω—ã—Ö
    const full = await eventStore.fetchEventById(card.id)
    if (!full) return
    form.data = JSON.parse(JSON.stringify(full))   // deep copy
    selectedEvent.value = full
    isCreatingNew.value = false
};

const createNewEvent = () => {
  form.data = {
    title: '–ù–æ–≤–æ–µ –º–µ—Ä–æ–ø—Ä–∏—è—Ç–∏–µ',
    description: '–û–ø–∏—Å–∞–Ω–∏–µ –º–µ—Ä–æ–ø—Ä–∏—è—Ç–∏—è (—Å—é–¥–∞ –º–æ–∂–Ω–æ –¥–æ–±–∞–≤–∏—Ç—å –ø—Ä–∞–≤–∏–ª–∞, –¥–æ–ø–æ–ª–Ω–∏—Ç–µ–ª—å–Ω—ã–µ —É—Å–ª–æ–≤–∏—è, –∏ —Ç.–¥.)',
    date: new Date().toLocaleDateString('ru-RU'),
    start_time: '09:00',
    end_time: '23:00',
    is_team: false,
    max_members: 1,
  };
  isCreatingNew.value = true;
  selectedEvent.value = form.data as IEventDetail;
};

const backToSelection = () => {
  selectedEvent.value = null;
  form.data = null;
  isCreatingNew.value = false;
};

const promptDeleteEvent = (event: IEventCard) => {
  modalConfig.type = 'confirm';
  modalConfig.title = '–ü–æ–¥—Ç–≤–µ—Ä–¥–∏—Ç–µ —É–¥–∞–ª–µ–Ω–∏–µ';
  modalConfig.message = `–í—ã —É–≤–µ—Ä–µ–Ω—ã, —á—Ç–æ —Ö–æ—Ç–∏—Ç–µ —É–¥–∞–ª–∏—Ç—å –º–µ—Ä–æ–ø—Ä–∏—è—Ç–∏–µ "${event.title}"? –≠—Ç–æ –¥–µ–π—Å—Ç–≤–∏–µ –Ω–µ–ª—å–∑—è –æ—Ç–º–µ–Ω–∏—Ç—å.`;
  modalConfig.onConfirm = () => deleteEvent(event.id);
  showModal.value = true;
};

const deleteEvent = async (id: number) => {
  try {
    await deleteEventApi(id);
    showModal.value = false; // –ó–∞–∫—Ä—ã–≤–∞–µ–º –æ–∫–Ω–æ –ø–æ–¥—Ç–≤–µ—Ä–∂–¥–µ–Ω–∏—è
    modalConfig.type = 'success';
    modalConfig.title = '–£—Å–ø–µ—à–Ω–æ';
    modalConfig.message = '–ú–µ—Ä–æ–ø—Ä–∏—è—Ç–∏–µ —É–¥–∞–ª–µ–Ω–æ.';
    showModal.value = true;
    await eventStore.fetchEvents(true);
  } catch (error: any) {
    showModal.value = false;
    modalConfig.type = 'error';
    modalConfig.title = '–û—à–∏–±–∫–∞';
    modalConfig.message = `–ù–µ —É–¥–∞–ª–æ—Å—å —É–¥–∞–ª–∏—Ç—å –º–µ—Ä–æ–ø—Ä–∏—è—Ç–∏–µ: ${error.message}`;
    showModal.value = true;
  }
};

// ===== –£–ü–†–ê–í–õ–ï–ù–ò–ï –í–ö–õ–ê–î–ö–ê–ú–ò =====

const switchToActivitiesTab = async (event: IEventCard) => {
  const full = await eventStore.fetchEventById(event.id);
  if (!full) return;
  selectedEventForActivities.value = full;
  activeTab.value = 'activities';
};

const selectEventForActivities = async (eventId: number) => {
  const full = await eventStore.fetchEventById(eventId);
  if (!full) return;
  selectedEventForActivities.value = full;
};

// ===== –£–ü–†–ê–í–õ–ï–ù–ò–ï –ê–ö–¢–ò–í–ù–û–°–¢–Ø–ú–ò =====

const addActivityToSelected = () => {
  if (!selectedEventForActivities.value) return;
  if (!selectedEventForActivities.value.activities) selectedEventForActivities.value.activities = [];
  selectedEventForActivities.value.activities.push({
    id: Date.now(), // –≤—Ä–µ–º–µ–Ω–Ω—ã–π ID –¥–ª—è –Ω–æ–≤—ã—Ö –∞–∫—Ç–∏–≤–Ω–æ—Å—Ç–µ–π
    name: '–ù–æ–≤–∞—è –∞–∫—Ç–∏–≤–Ω–æ—Å—Ç—å',
    icon: 'üéØ',
    latitude: 0,
    longitude: 0,
    is_scoreable: false,
    is_versus: false,
    max_score: 1,
    start_dt: null,
    end_dt: null
  });
};

const deleteActivityFromSelected = async (activity: any, index: number) => {
  if (!selectedEventForActivities.value?.activities) return;
  
  // –ï—Å–ª–∏ –∞–∫—Ç–∏–≤–Ω–æ—Å—Ç—å –∏–º–µ–µ—Ç —Ä–µ–∞–ª—å–Ω—ã–π ID (–Ω–µ –≤—Ä–µ–º–µ–Ω–Ω—ã–π), —É–¥–∞–ª—è–µ–º —Å —Å–µ—Ä–≤–µ—Ä–∞
  if (activity.id && activity.id < Date.now() - 1000000) { // –ø—Ä–æ–≤–µ—Ä—è–µ–º —á—Ç–æ —ç—Ç–æ –Ω–µ –≤—Ä–µ–º–µ–Ω–Ω—ã–π ID
    modalConfig.type = 'confirm';
    modalConfig.title = '–ü–æ–¥—Ç–≤–µ—Ä–¥–∏—Ç–µ —É–¥–∞–ª–µ–Ω–∏–µ';
    modalConfig.message = `–í—ã —É–≤–µ—Ä–µ–Ω—ã, —á—Ç–æ —Ö–æ—Ç–∏—Ç–µ —É–¥–∞–ª–∏—Ç—å –∞–∫—Ç–∏–≤–Ω–æ—Å—Ç—å "${activity.name}"?`;
    modalConfig.onConfirm = async () => {
      try {
        await deleteActivityApi(activity.id);
        selectedEventForActivities.value!.activities!.splice(index, 1);
        showModal.value = false;
        modalConfig.type = 'success';
        modalConfig.title = '–£—Å–ø–µ—à–Ω–æ';
        modalConfig.message = '–ê–∫—Ç–∏–≤–Ω–æ—Å—Ç—å —É–¥–∞–ª–µ–Ω–∞.';
        showModal.value = true;
        // –û–±–Ω–æ–≤–ª—è–µ–º –¥–∞–Ω–Ω—ã–µ –≤ store
        await eventStore.fetchEventById(selectedEventForActivities.value!.id, true);
      } catch (error: any) {
        showModal.value = false;
        modalConfig.type = 'error';
        modalConfig.title = '–û—à–∏–±–∫–∞';
        modalConfig.message = `–ù–µ —É–¥–∞–ª–æ—Å—å —É–¥–∞–ª–∏—Ç—å –∞–∫—Ç–∏–≤–Ω–æ—Å—Ç—å: ${error.message}`;
        showModal.value = true;
      }
    };
    showModal.value = true;
  } else {
    // –ü—Ä–æ—Å—Ç–æ —É–¥–∞–ª—è–µ–º –∏–∑ –ª–æ–∫–∞–ª—å–Ω–æ–≥–æ –º–∞—Å—Å–∏–≤–∞ –¥–ª—è –Ω–æ–≤—ã—Ö –∞–∫—Ç–∏–≤–Ω–æ—Å—Ç–µ–π
    selectedEventForActivities.value.activities.splice(index, 1);
  }
};

const saveActivities = async () => {
  if (!selectedEventForActivities.value?.activities) return;

  try {
    const eventId = selectedEventForActivities.value.id;
    
    // –û–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ–º –∞–∫—Ç–∏–≤–Ω–æ—Å—Ç–∏
    for (const activity of selectedEventForActivities.value.activities) {
      const { id: activityId, ...activityData } = activity;
      
      // –ü–æ–¥–≥–æ—Ç–∞–≤–ª–∏–≤–∞–µ–º –¥–∞–Ω–Ω—ã–µ –∞–∫—Ç–∏–≤–Ω–æ—Å—Ç–∏
      const cleanActivityData = {
        name: activityData.name,
        icon: activityData.icon,
        latitude: activityData.latitude || 0,
        longitude: activityData.longitude || 0,
        is_scoreable: activityData.is_scoreable || false,
        is_versus: activityData.is_versus || false,
        max_score: activityData.max_score || 1,
        start_dt: activityData.start_dt,
        end_dt: activityData.end_dt
      };
      
      if (activityId && activityId < Date.now() - 1000000) {
        // –û–±–Ω–æ–≤–ª—è–µ–º —Å—É—â–µ—Å—Ç–≤—É—é—â—É—é –∞–∫—Ç–∏–≤–Ω–æ—Å—Ç—å
        await updateActivityApi(activityId, cleanActivityData);
        console.log('–ê–∫—Ç–∏–≤–Ω–æ—Å—Ç—å –æ–±–Ω–æ–≤–ª–µ–Ω–∞:', activity.name);
      } else {
        // –°–æ–∑–¥–∞–µ–º –Ω–æ–≤—É—é –∞–∫—Ç–∏–≤–Ω–æ—Å—Ç—å
        await addActivityApi(eventId, cleanActivityData);
        console.log('–ê–∫—Ç–∏–≤–Ω–æ—Å—Ç—å –¥–æ–±–∞–≤–ª–µ–Ω–∞:', activity.name);
      }
    }
    
    // –û–±–Ω–æ–≤–ª—è–µ–º –¥–∞–Ω–Ω—ã–µ –≤ store
    await eventStore.fetchEventById(eventId, true);
    
    modalConfig.type = 'success';
    modalConfig.title = '–£—Å–ø–µ—à–Ω–æ!';
    modalConfig.message = '–ê–∫—Ç–∏–≤–Ω–æ—Å—Ç–∏ —Å–æ—Ö—Ä–∞–Ω–µ–Ω—ã.';
    showModal.value = true;
    
    // –û–±–Ω–æ–≤–ª—è–µ–º –ª–æ–∫–∞–ª—å–Ω—ã–µ –¥–∞–Ω–Ω—ã–µ
    await selectEventForActivities(eventId);
    
  } catch (error: any) {
    modalConfig.type = 'error';
    modalConfig.title = '–û—à–∏–±–∫–∞!';
    modalConfig.message = `–ù–µ —É–¥–∞–ª–æ—Å—å —Å–æ—Ö—Ä–∞–Ω–∏—Ç—å –∞–∫—Ç–∏–≤–Ω–æ—Å—Ç–∏: ${error.message}`;
    showModal.value = true;
  }
};

const saveEvent = async () => {
  if (!form.data) return;
  
  const validationError = validateEventForm();
  if (validationError) {
    modalConfig.type = 'error';
    modalConfig.title = '–û—à–∏–±–∫–∞ –≤–∞–ª–∏–¥–∞—Ü–∏–∏';
    modalConfig.message = validationError;
    showModal.value = true;
    return;
  }
  
  // –ü–æ–¥–≥–æ—Ç–∞–≤–ª–∏–≤–∞–µ–º –¥–∞–Ω–Ω—ã–µ –¥–ª—è –æ—Ç–ø—Ä–∞–≤–∫–∏ (–±–µ–∑ –º–µ–¥–∏–∞ –∏ –∞–∫—Ç–∏–≤–Ω–æ—Å—Ç–µ–π)
  const { media, leaderboard, activities, id, state, preview_url, ...eventData } = form.data!;

  if (eventData.date) {
    eventData.date = eventData.date.replace(/(\d{2})\.(\d{2})\.(\d{4})/, '$3-$2-$1'); 
  }
  
  if (eventData.max_teams == undefined) {
    delete eventData.max_teams; 
  }

  console.log(eventData)
  
  try {
    let eventId: number;
    
    if (isCreatingNew.value) {
      eventId = await createEventApi(eventData);
    } else {
      await updateEventApi(selectedEvent.value!.id, eventData);
      eventId = selectedEvent.value!.id;
      
      const existingMedia = selectedEvent.value!.media || [];
      for (const mediaItem of existingMedia) {
        try {
          await deleteEventMediaApi(eventId, mediaItem.id);
        } catch (error) {
          console.warn('–ù–µ —É–¥–∞–ª–æ—Å—å —É–¥–∞–ª–∏—Ç—å —Å—Ç–∞—Ä–æ–µ –º–µ–¥–∏–∞:', mediaItem.id);
        }
      }
    }
    
    // –î–æ–±–∞–≤–ª—è–µ–º –Ω–æ–≤—ã–µ –º–µ–¥–∏–∞
    const mediaTypes: Array<'image' | 'document'> = ['image', 'document'];
    for (const mediaType of mediaTypes) {
      const urls = (document.querySelector(`textarea[data-media-type="${mediaType}"]`) as HTMLTextAreaElement)?.value
        .split('\n')
        .filter(url => url.trim());

      for (let i = 0; i < urls.length; i++) {
        try {
          const url = urls[i].trim();
          const fileName = url.split('/').pop() || `${mediaType}_${i + 1}`;
          await addEventMediaApi(eventId, {
            url: url,
            media_type: mediaType,
            name: fileName,
            order: i
          });
          console.log(`–î–æ–±–∞–≤–ª–µ–Ω–æ ${mediaType}:`, url);
        } catch (error) {
          console.warn(`–ù–µ —É–¥–∞–ª–æ—Å—å –¥–æ–±–∞–≤–∏—Ç—å ${mediaType}:`, urls[i]);
        }
      }
    }
    
    // –û–±–Ω–æ–≤–ª—è–µ–º store
    await eventStore.fetchEvents(true);
    await eventStore.fetchEventById(eventId, true);

    modalConfig.type = 'success';
    modalConfig.title = '–£—Å–ø–µ—à–Ω–æ!';
    modalConfig.message = isCreatingNew.value ? '–ú–µ—Ä–æ–ø—Ä–∏—è—Ç–∏–µ —É—Å–ø–µ—à–Ω–æ —Å–æ–∑–¥–∞–Ω–æ.' : '–ò–∑–º–µ–Ω–µ–Ω–∏—è —É—Å–ø–µ—à–Ω–æ —Å–æ—Ö—Ä–∞–Ω–µ–Ω—ã.';
    showModal.value = true;

    backToSelection();

  } catch (error: any) {
    modalConfig.type = 'error';
    modalConfig.title = '–û—à–∏–±–∫–∞!';
    modalConfig.message = `–ù–µ —É–¥–∞–ª–æ—Å—å —Å–æ—Ö—Ä–∞–Ω–∏—Ç—å –¥–∞–Ω–Ω—ã–µ: ${error.message}`;
    showModal.value = true;
  }
}

onMounted(() => {
  eventStore.fetchEvents();
});
</script>

